
/**
 * add - to aggregate values
 * @a: fist int
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
return (a + b);
}

/**
 * mul - multiply values
 * @a: first int
 * @b: second int
 * Return: multiplication
 */
int mul(int a, int b)
{
return (a * b);
}

/**
 * mod - mod integers
 * @a: first int
 * @b: second int
 * Return: final answer
 */
int mod(int a, int b)
{
return (a % b);
}

/**
 * sub - subtract integers
 * @a: first int
 * @b: second int
 * Return: subtraction
 */
int sub(int a, int b)
{
return (a - b);
}

